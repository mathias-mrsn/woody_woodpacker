uint8_t *
get_unpacker (  const void * old_start,
                const void * key,
                const void * text,
                const uint64_t key_len,
                const uint64_t text_len,
                const bool     is_64)
{
    uint8_t * unpacker = NULL;

    if (is_64 == true)
    {
        unpacker = calloc(1, RC4_UNPACKER_SIZE);
        if (unpacker == NULL) {
            return (NULL);
        }

        memcpy(unpacker, rc4_64_unpacker, RC4_UNPACKER_SIZE);
        memcpy((unpacker + RC4_UNPACKER_SIZE) - OLD_START_REVERSE_OFFSET_64, &old_start, sizeof(void *)); 
        memcpy((unpacker + RC4_UNPACKER_SIZE) - TEXT_ADDR_REVERSE_OFFSET_64, &text, sizeof(void *)); 
        memcpy((unpacker + RC4_UNPACKER_SIZE) - TEXT_LEN_REVERSE_OFFSET_64, &text_len, sizeof(uint64_t)); 
        memcpy((unpacker + RC4_UNPACKER_SIZE) - KEY_ADDR_REVERSE_OFFSET_64, &key, sizeof(void*));
        memcpy((unpacker + RC4_UNPACKER_SIZE) - KEY_LEN_REVERSE_OFFSET_64, &key_len, sizeof(uint64_t)); 

        for(int i = 0; i < RC4_UNPACKER_SIZE; i++) {
            if (i > 0 && i % 8 == 0) {
                printf("\n");
            }
            printf("0x%02x ", unpacker[i]);
        }
        printf("\n");
    } else {}


    return (unpacker);
}


